int		ft_sqrt(int nb)
{
	int		i;

	i = 0;
	if (nb == 0)
		return (0);
	if (nb == 1)
		return (1);
	while (i < 46341)
	{
		if (nb == i * i)
			return (i);
		else
			i++;
	}
	return (0);
}
